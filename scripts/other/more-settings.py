import os
from sys import stdout

#------------------------------PRINT_COLORS------------------------------
def red():
    RED = "\033[1;31m"
    stdout.write(RED)
def blue():
    BLUE = "\033[1;34m"
    stdout.write(BLUE)
def white():
    WHITE = "\033[1;37m"
    stdout.write(WHITE)
def green():
    GREEN = "\033[1;32m"
    stdout.write(GREEN)

#------------------------------SCRIPT------------------------------
def apiconfig():
    os.system("clear")

    control_center_r = open('config/control-center','r')
    control_center = control_center_r.read()
    control_center_r.close()

    green();print("Opening configuration APP")

    os.system(f"{control_center}")


if __name__ == '__main__':
    apiconfig()